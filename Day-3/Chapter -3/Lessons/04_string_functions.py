name = "hello sijan mahmud"

# len function
length = len(name) #got the length of name variable
print(length)

# endswith function
print(name.endswith("ud")) # this will give boolean value and this is also case sensetive

# startswith function
print(name.startswith("he")) # this will give boolean value and this is also case sensetive

#capitalize function
print(name.capitalize()) #this will capitalize the first word of the string

# find funciton
print(name.find("sijan")) # this will give you the first index of that word or latter

# replace function
print(name.replace("sijan","arafat")) # in first peremeter it takes what do you want to replace and the second peremeter it takes what you want to replace with


